export const ValidateMobileNumber = (mobileNumber) => {
  const reg = /^\d+$/;
  if (!reg.test(mobileNumber.toString())) {
    return false;
  }
  return true;
};

export const ValidateMobileNumberLength = (mobileNumber) => {
  const length = mobileNumber.length;
  if (length < 10 || length > 10) {
    return false;
  }
  return true;
};

